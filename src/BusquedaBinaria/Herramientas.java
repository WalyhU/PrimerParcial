package BusquedaBinaria;

public class Herramientas {
    private String nombre;
    private Integer id;

    public Herramientas(String nombre, Integer id) {
        this.nombre = nombre;
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public Integer getId() {
        return id;
    }
}
